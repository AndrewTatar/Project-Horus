    private class MakeRequestTask extends AsyncTask<Void, Void, ArrayList<IEvent>> {
        private Drive mService = null;
        private Exception mLastError = null;

        public MakeRequestTask(GoogleAccountCredential credential) {
            HttpTransport transport = AndroidHttp.newCompatibleTransport();
            JsonFactory jsonFactory = JacksonFactory.getDefaultInstance();
            mService = new Drive.Builder(
                    transport, jsonFactory, credential)
                    .setApplicationName("Drive API Android Quickstart")
                    .build();
        }

        @Override
        protected ArrayList<IEvent> doInBackground(Void... params) {
            try {
                return getDataFromApi();
            } catch (Exception e) {
                mLastError = e;
                cancel(true);
                return null;
            }
        }

        private ArrayList<IEvent> getDataFromApi() throws IOException {
            ArrayList<IEvent> items = new ArrayList<IEvent>();

            String pageToken = null;

            FileList result = mService.files().list()
                    .setQ("name='HORUS Security' and mimeType='application/vnd.google-apps.folder'")
                    .setFields("files(id, name)")
                    .setPageToken(pageToken)
                    .execute();

            List<File> files = result.getFiles();

            String folderID = "";

            if (files != null) {
                for (File file : files) {
                    folderID = file.getId();
                    break;
                }
            }

            if (!folderID.equals("")) {
                pageToken = null;
                result = mService.files().list()
                        .setQ("'" + folderID + "' in parents and mimeType='image/png'")
                        .setFields("files(createdTime, description, id, mimeType, name, thumbnailLink, webContentLink)")
                        .setOrderBy("createdTime desc")
                        .execute();

                files = result.getFiles();

                if (files != null) {
                    for (File file : files) {
                        IEvent i = new IEvent();
                        i.ID = file.getId();
                        i.Name = file.getName();
                        i.Description = file.getDescription();
                        i.Created = file.getCreatedTime();
                        i.Thumbnail = file.getThumbnailLink();
                        i.WebLink = file.getWebContentLink();

                        items.add(i);
                    }
                }
            }

            return items;
        }

        @Override
        protected void onPreExecute() {
            mProgress.show();
        }

        @Override
        protected void onPostExecute(ArrayList<IEvent> output) {
            mProgress.hide();
            if (output == null || output.size() == 0) {
                Toast.makeText(MainActivity.this, "No Logged Events Found.", Toast.LENGTH_SHORT).show();
            } else {
                //Output data to UI
                ResultsAdapter adapter = new ResultsAdapter(MainActivity.this, output);
                resultsList.setAdapter(adapter);
            }
        }

        @Override
        protected void onCancelled() {
            mProgress.hide();
            if (mLastError != null) {
                if (mLastError instanceof GooglePlayServicesAvailabilityIOException) {
                    showGooglePlayServicesAvailabilityErrorDialog(
                            ((GooglePlayServicesAvailabilityIOException) mLastError)
                                    .getConnectionStatusCode());
                } else if (mLastError instanceof UserRecoverableAuthIOException) {
                    startActivityForResult(
                            ((UserRecoverableAuthIOException) mLastError).getIntent(),
                            MainActivity.REQUEST_AUTHORIZATION);
                } else {
                    Toast.makeText(MainActivity.this, "An error has occured. " + mLastError.getMessage(), Toast.LENGTH_SHORT).show();
                }
            } else {
                Toast.makeText(MainActivity.this, "Request Cancelled", Toast.LENGTH_SHORT).show();
            }
        }
    }
